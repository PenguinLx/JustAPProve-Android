package com.example.justapproveroberto;

public class RankingAdicionar {
    private String perfilImagem = "";
    private String apelido = "";
    private String pontuacao = "";

    public void setPerfilImagem(String perfilImagem) {
        this.perfilImagem = perfilImagem;
    }

    public String getPerfilImagem() {
        return perfilImagem;
    }

    public void setApelido(String apelido) {
        this.apelido = apelido;
    }

    public String getApelido() {
        return apelido;
    }

    public void setPontuacao(String pontuacao) {
        this.pontuacao = pontuacao;
    }

    public String getPontuacao() {
        return pontuacao;
    }
}
