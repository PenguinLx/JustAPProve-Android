package br.ifsul.justapprove.retrofit;

public class LoginResponse {

    private boolean resposta;
    private Integer id;
    private String apelido;
    private int pontos;

    public boolean getResposta() {
        return resposta;
    }

    public Integer getId() {
        return id;
    }

    public String getApelido() {
        return apelido;
    }

    public int getPontos() {
        return pontos;
    }
}


